public class chr {
    public static void main(String[] args) {
    // here its used to print the ascii values
        System.out.println('a'+'b');
    // here its used to print the strings
        System.out.println("a"+"b");
    // here its used to print the value
        System.out.println('a'+3);
    // here its used to print the ascii key value addition
        System.out.println((char) ('a'+3));
        System.out.println('a'+1);
        System.out.println("a"+1);
    }
}
